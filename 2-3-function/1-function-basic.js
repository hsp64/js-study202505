
//함수 정의 : 함수를 만드는 과정 - 코드가 실행되지 않습니다.
function sayHello(userName) {
    console.log(`${userName}님 환영합니다.`);
    console.log(`오늘도 즐거운 하루 되세요!!`);
}

// 함수 호출 : 함수를 이제 실행시키는 과정
sayHello('홍길동');
sayHello('티니핑');


